function draw(selectedPollutant, polvalue) {
  d3v3.json("INDIA_STATES.json", function (json) {
    var maxTotal = CO;
    if (selectedPollutant === "PM2.5") maxTotal = PM2;
    else if (selectedPollutant === "CO") maxTotal = CO;
    else if (selectedPollutant === "NO2") maxTotal = NO2;
    else if (selectedPollutant === "Ozone") maxTotal = O3;
    else if (selectedPollutant === "SO2") maxTotal = SO2;
    else if (selectedPollutant === "PM10") maxTotal = PM10;
    // var

    if (selectedPollutant === "PM2.5") dataset = data_PM2;
    if (selectedPollutant === "CO") dataset = data_CO;
    if (selectedPollutant === "NO2") dataset = data_NO2;
    if (selectedPollutant === "Ozone") dataset = data_O3;
    if (selectedPollutant === "SO2") dataset = data_SO2;
    if (selectedPollutant === "PM10") dataset = data_PM10;

    var colorScale = d3v3.scale
      .quantile()
      .domain(
        d3v3.range(buckets).map(function (d) {
          if (selectedPollutant === "PM10") return (d / buckets) * 200;
          if (selectedPollutant === "CO") return (d / buckets) * 2;
          if (selectedPollutant === "NO2") return (d / buckets) * 50;
          if (selectedPollutant === "SO2") return (d / buckets) * 30;
          if (selectedPollutant === "PM2.5") return (d / buckets) * 90;
          if (selectedPollutant === "Ozone") return (d / buckets) * 70;
        })
      )
      .range(colors);

    india
      .selectAll("path")
      .data(json.features)
      .enter()
      .append("path")
      .attr("d", path)
      .style("fill", colors[0])
      .style("opacity", 0.7)

      .on("click", function (d, i) {
        d3v3.select(this).transition().duration(3000).style("opacity", 1);
        div.transition().duration(300).style("opacity", 1);

        var state = d.properties.STATE;
        selectedState = humanize(state);

        div
          .text(
            humanize(d.properties.STATE) +
              " : " +
              dataset[state][selectedMonth][selectedYear]
          )

          .style("left", d3v3.event.pageX + "px")
          .style("top", d3v3.event.pageY - 30 + "px")
          .style("font-size", "16px") // Adjust the font size here
          .style("font-family", "Arial, sans-serif") // Adjust the font family here
          .style("color", "black") // Adjust the text color here
          .style("background-color", "lightgray") // Adjust the background color here
          .style("padding", "8px") // Adjust padding here
          .style("border-radius", "8px"); // Adjust border radius here
        draw_timeSeries(humanize(state), globalP);
        plotParallelCoordinates(humanize(state));
        updateMethod2(humanize(state));
      })

      .on("mouseleave", function (d, i) {
        d3v3.select(this).transition().duration(3000).style("opacity", 0.7);
        div.transition().duration(3000).style("opacity", 0);
      })
      .on("mouseenter", function (d, i) {
        d3v3.select(this).transition().duration(300).style("opacity", 1);
        div.transition().duration(300).style("opacity", 0);
      });
    const non_existant_data = [
      "ladakh",
      "andaman_and_nicobar_islands",
      "goa",
      "lakshadweep",
      "manipur",
      "mizoram",
      "dadar_and_nagar_haveli",
      "daman_and_diu",
    ];
    india
      .selectAll("path")
      .transition()
      .duration(1000)
      .style("fill", function (d) {
        if (non_existant_data.includes(d.properties.STATE)) {
          return colorScale(0.0);
        }
        return colorScale(
          dataset[d.properties.STATE][selectedMonth][selectedYear]
        );
      });

    // Adding legend for our Choropleth
    var g = india
      .append("g")
      .attr("class", "key")
      .attr("transform", "translate(680, 305)");
    // .call(yAxis);

    // Define zoom behavior
    var zoom = d3v3.behavior.zoom().on("zoom", zoomed);

    // Apply zoom behavior to the map
    map.call(zoom);

    function zoomed() {
      india.attr(
        "transform",
        "translate(" + d3v3.event.translate + ")scale(" + d3v3.event.scale + ")"
      );
    }
  });
}

